class coupon {

    id
    label
    code
    start_date
    end_date
    id_type

    constructor(id, label, code, start_date, end_date, id_type)
    {
        this.id = id;
        this.label = label;
        this.code = code;
        this.start_date = start_date;
        this.end_date = end_date;
        this.id_type = id_type;
    }
}
